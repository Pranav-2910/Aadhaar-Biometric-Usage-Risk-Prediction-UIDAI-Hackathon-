"""***Machine Learning Model***

For usage risk prediction
"""

# Encoding states and districts and risk levels
from sklearn.preprocessing import LabelEncoder
le_state = LabelEncoder()
le_district = LabelEncoder()
le_risk = LabelEncoder()
df["state_enc"] = le_state.fit_transform(df["state"])
df["district_enc"] = le_district.fit_transform(df["district"])
df["risk_level"] = le_risk.fit_transform(df["risk_level"])

# Feature matrix
X = df[["state_enc","district_enc","pincode","day","month","weekday"]]
y = df["risk_level"]

# Splitting training and testing data
from sklearn.model_selection import train_test_split
X_train,X_test,y_train,y_test = train_test_split(X,y,test_size = 0.20,random_state = 42)

# Training model
from sklearn.ensemble import RandomForestClassifier
model = RandomForestClassifier(n_estimators=400, max_depth = 20)
model.fit(X_train, y_train)

# Evaluating the model
from sklearn.metrics import accuracy_score, classification_report
y_pred = model.predict(X_test)
print("Accuracy : ",accuracy_score(y_test, y_pred))
print("Classification_Report : \n\n", classification_report(y_test, y_pred))

# Checking for feature importance
feature_importance = pd.Series(model.feature_importances_, index=X.columns).sort_values()
feature_importance.plot(kind="barh")
plt.title("Feature Importance")
plt.show()
print("\n\n")
print("Feature Importance : \n",feature_importance)

# Prediction function
def predict_risk(state,district,pincode,day,month,weekday):
  state_enc = le_state.transform([state])[0]
  district_enc = le_district.transform([district])[0]
  input = [[state_enc,district_enc,pincode,day,month,weekday]]
  risk_level_predict = model.predict(input)[0]
  return le_risk.inverse_transform([risk_level_predict])[0]

